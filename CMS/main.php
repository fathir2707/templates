<p align="left" style="line-height:24px; padding:10px;">Sistem Manajemen Keluhan Pelanggan ini adalah proyek berbasis web yang dikembangkan di PHP / MySQL yang digunakan untuk mengelola keluhan pelanggan secara online. Pengguna dapat login, dan membuat keluhan. Sistem ini memiliki satu modul Admin di mana Admin dapat login dan melihat semua rincian keluhan, dan memberi penugasan kepada tim kepuasan pelanggan untuk menetapkan status keluhan tersebut.</p>
